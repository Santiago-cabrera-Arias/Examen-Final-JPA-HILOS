    private void btnAgregar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregar1ActionPerformed

        String ingreso = JOptionPane.showInputDialog("Ingrese el numero de cedula de la puerca");

        if (ingreso.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Llene el campo");

        } else {
            Persona persona = controladorPersona.buscar(ingreso);

            if (persona != null) {

                lista.set(0, new JuegoClasico(banco, descuento, txtArea, persona, txtNumero1));
                lblcliente1.setVisible(true);

            } else {

                JOptionPane.showMessageDialog(this, "no existe un jugador con el numero de cedula " + ingreso);

            }

        }


    }//GEN-LAST:event_btnAgregar1ActionPerformed

