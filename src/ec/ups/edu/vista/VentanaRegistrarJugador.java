    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        long id = Long.parseLong(txtId.getText());
        String cedula = txtCedula.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        int cuenta = Integer.parseInt(txtCuenta.getText());

        Persona p = new Persona();

        p.setId(id);
        p.setCedula(cedula);
        p.setNombre(nombre);
        p.setApellido(apellido);
        p.setCuenta(cuenta);

        boolean cent = controladorPersona.delete(p);

        if (cent == true) {

            JOptionPane.showMessageDialog(this, "Se ha eliminado con exito");
            listarTodo();

        } else {

            JOptionPane.showMessageDialog(this, "No se ha podido eliminar");

        }


    }//GEN-LAST:event_btnEliminarActionPerformed


