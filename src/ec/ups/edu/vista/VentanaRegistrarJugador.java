    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        String cedula = txtCedula.getText();

        if (cedula.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Llene el campo");

        } else {

            Persona persona = controladorPersona.buscar(cedula);

            if (persona.getCedula().equals(cedula)) {

                listarDatos(persona);

            } else if (!persona.getCedula().equals(cedula)) {

                JOptionPane.showMessageDialog(this, "no existe un jugador con el numero de cedula " + cedula);

            }

        }


    }//GEN-LAST:event_btnBuscarActionPerformed

