    public void listarDatos(Persona persona) {

        DefaultTableModel modelo = (DefaultTableModel) tblJugadores.getModel();
        modelo.setRowCount(0);

        Object[] objeto = {persona.getId(), persona.getCedula(), persona.getNombre(), persona.getApellido(), persona.getCuenta()};
        modelo.addRow(objeto);

        tblJugadores.setModel(modelo);
    }

    public void listarTodo() {

        DefaultTableModel modelo = (DefaultTableModel) tblJugadores.getModel();
        modelo.setRowCount(0);

        List<Persona> listaPersona = controladorPersona.findAll();

        if (listaPersona.isEmpty()) {

            JOptionPane.showMessageDialog(this, "No existen datos en la tabla");

        } else {

            for (Persona persona1 : listaPersona) {

                Object[] objeto = {persona1.getId(), persona1.getCedula(), persona1.getNombre(),
                    persona1.getApellido(), persona1.getCuenta()};

                modelo.addRow(objeto);
            }

            tblJugadores.setModel(modelo);

        }

    }


    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        String cedula = txtCedula.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        int cuenta = Integer.parseInt(txtCuenta.getText());

        if (cedula.isEmpty() || nombre.isEmpty() || apellido.isEmpty() || String.valueOf(cuenta).isEmpty()) {

            JOptionPane.showMessageDialog(this, "Llene los campos");

        } else {

            persona.setCedula(cedula);
            persona.setNombre(nombre);
            persona.setApellido(apellido);
            persona.setCuenta(cuenta);

            boolean cent = controladorPersona.create(persona);

           
            if (cent == true) {

                JOptionPane.showMessageDialog(this, "Se ha registrado con exito.");

            } else {

                JOptionPane.showMessageDialog(this, "No se ha podido registrar.");

            }

        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

